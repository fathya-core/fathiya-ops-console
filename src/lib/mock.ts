import type { MarketIntelOutput, BugBountyOutput } from '../types';

export function generateMarketIntel(input: {
  asset: string;
  timeframe: string;
  marketContext?: string;
  dataSource: string;
  riskLevel: string;
  notes: string;
}): MarketIntelOutput {
  const { asset, timeframe, riskLevel } = input;
  const asset_ = asset || 'الأصل';
  const tf = timeframe || 'الإطار الزمني';
  const riskMap: Record<string, number> = { low: 72, medium: 61, high: 48 };
  const score = riskMap[riskLevel] ?? 60;

  return {
    coreThesis: `الفرضية المركزية على ${asset_} ضمن إطار ${tf}: البنية الحالية تُظهر تراكماً هادئاً مع ضعف في زخم البائعين. السيناريو الأساسي يفترض استمرار النطاق العرضي قبل اختيار الاتجاه، مع ميل طفيف نحو الاختراق الصاعد إذا استمر حجم التداول في الأعلى.`,
    bullishScenario: `اختراق مستوى المقاومة القريب مدعوماً بحجم أعلى من المتوسط يفتح الطريق نحو المنطقة العليا التالية. تأكيد إضافي عبر إعادة الاختبار الناجح للمقاومة المكسورة كدعم جديد، مع توسع في طبقات السيولة.`,
    bearishScenario: `فشل في تثبيت السعر فوق منطقة التوازن وتشكّل قمة أدنى يرجح العودة إلى قاع النطاق. كسر الدعم الرئيسي بإغلاق مؤكد يُفعّل سيناريو التصحيح الأعمق نحو منطقة الطلب الأسبق.`,
    invalidation: [
      'إغلاق حاسم أسفل مستوى الدعم الهيكلي الرئيسي',
      'ارتفاع حاد في تقلب الأصل يتجاوز الانحراف المعياري المعتاد',
      'تدفقات مخالفة واضحة من محافظ كبيرة على السلسلة',
      'تغيّر جوهري في الظرف الماكرو (أسعار فائدة، سيولة)',
    ],
    earlyWarnings: [
      'انحراف سلبي في مؤشر الزخم مقابل السعر',
      'انكماش في عمق دفتر الأوامر عند المستويات الحرجة',
      'ارتفاع نسبة البيع على البيع عبر التداولات الكبيرة',
      'هبوط مؤشر القوة النسبية تحت ٤٠ على إطار أعلى',
    ],
    confidenceScore: score,
    hiddenRisk: `المخاطرة الخفية: الارتباط غير المرئي بأصول أخرى قد يُضخّم الحركات في حال حدوث صدمة سيولة. مستوى المخاطرة المُدخل: ${riskLevel}.`,
    nextDataNeeded: [
      'تدفقات العملات المستقرة على البورصات الرئيسية (٢٤ ساعة)',
      'بيانات دفتر أوامر عميق عند مستويات الدعم/المقاومة القريبة',
      'مؤشرات تمويل العقود الآجلة الدائمة واهتمام مفتوح',
      'مفكرة اقتصادية ماكرو لليوم التالي (CPI/FOMC إن وُجد)',
    ],
  };
}

export function generateBugBounty(input: {
  programName: string;
  policyUrl: string;
  allowedScope: string;
  forbiddenScope: string;
  assets: string;
  notes: string;
}): BugBountyOutput {
  const inScope = input.allowedScope.split(/[\n,]/).map(s => s.trim()).filter(Boolean);
  const outScope = input.forbiddenScope.split(/[\n,]/).map(s => s.trim()).filter(Boolean);
  const assets = input.assets.split(/[\n,]/).map(s => s.trim()).filter(Boolean);

  return {
    scopeMap: {
      in: inScope.length ? inScope : ['لم تُحدَّد نطاقات مسموحة'],
      out: outScope.length ? outScope : ['لم تُحدَّد نطاقات ممنوعة'],
    },
    assetInventory: (assets.length ? assets : ['asset-غير-محدد']).map((name, i) => ({
      name,
      type: i % 3 === 0 ? 'Web App' : i % 3 === 1 ? 'API' : 'Mobile',
      priority: i === 0 ? 'عالية' : i === 1 ? 'متوسطة' : 'منخفضة',
    })),
    vulnHypotheses: [
      {
        title: 'احتمال IDOR في نقاط الوصول للموارد الخاصة',
        rationale: 'استناداً إلى نمط المعرفات التسلسلية الظاهر في الواجهة، ينبغي التحقق من التحكم بالصلاحيات على مستوى الكائن.',
        severity: 'عالية',
      },
      {
        title: 'فحص سياسات CORS المتراخية',
        rationale: 'رؤوس CORS قد تسمح بأصول متعددة؛ يستحق الاختبار الآمن على البيئة المسموح بها فقط.',
        severity: 'متوسطة',
      },
      {
        title: 'تسريب معلومات عبر رسائل الأخطاء',
        rationale: 'كشف مسارات أو آثار مكدّس محتمل في استجابات الخطأ القياسية.',
        severity: 'منخفضة',
      },
    ],
    safeChecklist: [
      'قراءة سياسة البرنامج كاملة وتأكيد الالتزام بالنطاق المسموح فقط',
      'عدم تنفيذ أي اختبار فعلي — المسودة للتحليل والتخطيط فقط',
      'استخدام حسابات اختبارية مخصصة حين يُسمح بذلك من البرنامج',
      'تجنّب هجمات الحرمان من الخدمة والاختبارات التي تؤثر على مستخدمين حقيقيين',
      'توثيق كل خطوة مخططة بشكل قابل لإعادة الإنتاج قبل أي تنفيذ بشري',
      'إيقاف فوري عند الاقتراب من أي أصل خارج النطاق',
    ],
    evidenceTemplate: `# قالب الأدلة

## العنوان
<وصف موجز للفرضية>

## الأصل المتأثر
<الأصل من قائمة الجرد>

## الخطوات المخططة (مسودة فقط)
1. <خطوة>
2. <خطوة>

## الأثر المتوقع
<وصف الأثر دون تنفيذ>

## دليل المفهوم (نصي فقط)
<وصف نظري — لا يُنفَّذ>

## التوصية
<توصية الإصلاح>
`,
    reportDraft: `# مسودة تقرير — ${input.programName || 'برنامج غير مسمى'}

> حالة: مسودة — لم يُنفَّذ أي اختبار فعلي.
> سياسة البرنامج: ${input.policyUrl || 'غير محددة'}

## الملخص التنفيذي
تحليل أولي للنطاق والأصول المعلنة ضمن البرنامج، مع فرضيات ثغرات مرتبة حسب الأولوية. هذه المسودة للمراجعة البشرية قبل أي إجراء خارجي.

## النطاق
- مسموح: ${inScope.join('، ') || '—'}
- ممنوع: ${outScope.join('، ') || '—'}

## الأصول
${(assets.length ? assets : ['—']).map(a => `- ${a}`).join('\n')}

## ملاحظات المحلل
${input.notes || '—'}

## الخطوة التالية
تأكيد بشري صريح قبل أي تنفيذ خارج حدود التحليل.`,
  };
}
